package AST.Elements;

public class HtmlComment {
    private String htmlComment;
    private String htmlConditionalComment;

    public String getHtmlComment() {
        return htmlComment;
    }

    public void setHtmlComment(String htmlComment) {
        this.htmlComment = htmlComment;
    }

    public String getHtmlConditionalComment() {
        return htmlConditionalComment;
    }

    public void setHtmlConditionalComment(String htmlConditionalComment) {
        this.htmlConditionalComment = htmlConditionalComment;
    }
}
