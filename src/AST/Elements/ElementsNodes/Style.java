package AST.Elements.ElementsNodes;

public class Style {
    private String styleOpen;
    private String styleBody;
    private String styleShort;

    public String getStyleOpen() {
        return styleOpen;
    }

    public void setStyleOpen(String styleOpen) {
        this.styleOpen = styleOpen;
    }

    public String getStyleBody() {
        return styleBody;
    }

    public void setStyleBody(String styleBody) {
        this.styleBody = styleBody;
    }

    public String getStyleShort() {
        return styleShort;
    }

    public void setStyleShort(String styleShort) {
        this.styleShort = styleShort;
    }
}
