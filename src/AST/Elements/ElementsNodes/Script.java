package AST.Elements.ElementsNodes;

public class Script {
    private String scriptOpen;
    private String scriptBody;
    private String scripShortBody;

    public String getScriptOpen() {
        return scriptOpen;
    }

    public void setScriptOpen(String scriptOpen) {
        this.scriptOpen = scriptOpen;
    }

    public String getScriptBody() {
        return scriptBody;
    }

    public void setScriptBody(String scriptBody) {
        this.scriptBody = scriptBody;
    }

    public String getScripShortBody() {
        return scripShortBody;
    }

    public void setScripShortBody(String scripShortBody) {
        this.scripShortBody = scripShortBody;
    }
}
